/// <summary>
/// The current game mode being run by the application.
/// </summary>
public enum GameMode
{
    /// <summary>
    /// Player is currently in the main menu.
    /// </summary>
    MAIN_MENU,

    /// <summary>
    /// Player is currently in game mode.
    /// </summary>
    GAME,

    /// <summary>
    /// Player is currently in simulation mode.
    /// </summary>
    SIMULATION,
}
